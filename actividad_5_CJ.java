public class actividad_5_CJ {
    public static void main (String[] args){
        //Declarar e imprimir variables
        int q = 29;
        float p = 3.1416f;
        System.out.println("p = 29");
        System.out.println("q = 3.1416");

        // Suma
        float suma = q + p;
        System.out.println("El resultado de la operación 'q + p' es = " + suma);

        // Resta
        float resta = q - p;
        System.out.println("El resultado de la operación 'q - p' es = " + resta);

        // Multiplicación
        double multiplicacion = q * p;
        System.out.println("El resultado de la operación 'q * p' es = " + multiplicacion);

        // División
        double division = q / p;
        System.out.println("El resultado de la operación 'q / p' es = " + division);

        // Módulo (residuo division)
        double modulo = q % p;
        System.out.println("El resultado de la operación 'q % p' es = " + modulo);

    }
}
